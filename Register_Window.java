    private void jButtonSelectImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectImageActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser(); // swing package provide JFileChooser class for file slection
        chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("*images","jpg","png"); // set extensions as required
        chooser.addChoosableFileFilter(fnef); // adding extension filter to JFileChooser
        int ans = chooser.showSaveDialog(null); // ans get values as  ( yes, ok or cancel ) is selected
        if(ans == JFileChooser.APPROVE_OPTION){  // APPROVE_OPTION is = 0 if selected
            File f = chooser.getSelectedFile(); 
            String path = f.getAbsolutePath();
            label_photo.setIcon(resize_Image(path,null));
            this.photopath = path;
        }else{
            System.out.println("Image not Selected!");
        }
    }//GEN-LAST:event_jButtonSelectImageActionPerformed

