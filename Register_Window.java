    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        // TODO add your handling code here:
        
        String name = jTextFieldName.getText();
        String email = jTextFieldEmail.getText();
        String pass = jPasswordFieldPassword.getText();
        String cpass = jPasswordFieldConfirmPassword.getText();
        String mob = jTextFieldMobile.getText();
        String gender = "Male";
        if(jRadioButtonFemale.isSelected())
            gender = "Female";
        java.sql.Date dob = new java.sql.Date(jDateChooserDOB.getDate().getTime());
        // validation
        
        if(name.trim().equals("") || email.trim().equals("") || pass.trim().equals("") || cpass.trim().equals("")
                || mob.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Kindly fill all the fields");
        }
        else if( pass.equals(cpass) )
        {
            if(dob == null){
                JOptionPane.showMessageDialog(null,"Select Date of Birth!");
                jDateChooserDOB.grabFocus();
            }
            else{
                // insert into the database table
                
                PreparedStatement pst = null;
                ResultSet rs = null;
                
                Connection con = myc.getConn();
                try{
                    // check email already registered or not otherwise SQLException will occur
                    String sql = "select name from users where email like ?";
                    pst = con.prepareStatement(sql);
                    pst.setString(1,email);
                    rs = pst.executeQuery();
                    if(rs.next()){
                        // email already exists
                        JOptionPane.showMessageDialog(null, "Email already in use, try to login");
                        jTextFieldEmail.setText("");
                        jTextFieldEmail.grabFocus();
                    }
                    else{// insertion query here
                        sql = "insert into users(name,email,password,mobile,gender,dob,photo) values(?,?,?,?,?,?,?)";
                        pst = con.prepareStatement(sql);
                        pst.setString(1, name);
                        pst.setString(2,email);
                        pst.setString(3, pass);
                        pst.setString(4,mob);
                        pst.setString(5,gender);
                        pst.setDate(6,dob);
                        InputStream is = new FileInputStream(new File(photopath));
                        pst.setBlob(7, is);
                        int i = pst.executeUpdate();        
                        if(i>=1){
                            JOptionPane.showMessageDialog(null, "Registration Successfull...");
                            
                        }else{
                            jButtonResetActionPerformed(evt);
                            JOptionPane.showMessageDialog(null,"Registration Failed!");
                        }
                    }
                    
                }catch(HeadlessException | FileNotFoundException | SQLException e){
                    JOptionPane.showMessageDialog(null, "Database connection error" + e);
                }
                finally{
                    if (rs != null) try { rs.close(); } catch (SQLException ignore) {}
                    if (pst != null) try { pst.close(); } catch (SQLException ignore) {}
                    if (con != null) try { con.close(); } catch (SQLException ignore) {}
                }
                
            }
        }
        else
        {
            jPasswordFieldConfirmPassword.setText("");
            JOptionPane.showMessageDialog(null,"confirm password didn't match with your password");
            jPasswordFieldConfirmPassword.grabFocus();
        }
        
    }//GEN-LAST:event_jButtonRegisterActionPerformed

